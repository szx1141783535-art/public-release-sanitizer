RULES = compile_rules()
VALID_SEVERITIES = {"high", "medium", "low"}


def normalize_path(path: Path) -> str:
    return str(path.resolve())


def is_text_path(path: Path) -> bool:
    return path.suffix.lower() in TEXT_EXTENSIONS or path.name.lower() in TEXT_FILENAMES_LOWER


def is_supported_path(path: Path) -> bool:
    return is_text_path(path) or path.suffix.lower() in OFFICE_EXTENSIONS or path.suffix.lower() in PDF_EXTENSIONS


def has_skipped_parent(path: Path, root: Path) -> bool:
    try:
        parts = path.relative_to(root).parts[:-1]
    except ValueError:
        parts = path.parts[:-1]
    return any(part.lower() in SKIP_DIR_NAMES for part in parts)


def collect_files(inputs: list[str]) -> tuple[list[Path], list[ExtractionIssue]]:
    files: list[Path] = []
    issues: list[ExtractionIssue] = []
    for item in inputs:
        path = Path(item)
        if not path.exists():
            issues.append(ExtractionIssue(item, "MISSING_INPUT", "Input path does not exist."))
            continue
        if path.is_dir():
            for child in path.rglob("*"):
                if has_skipped_parent(child, path):
                    continue
                if child.is_file() and is_supported_path(child):
                    files.append(child)
        elif path.is_file():
            if is_supported_path(path):
                files.append(path)
            else:
                issues.append(
                    ExtractionIssue(
                        normalize_path(path),
                        "UNSUPPORTED_FILE",
                        f"Explicit input has unsupported extension: {path.suffix or '<none>'}.",
                    )
                )
    return sorted(set(files), key=lambda p: str(p).lower()), issues


def read_text_file(path: Path) -> list[tuple[str, str]]:
    data = path.read_bytes()
    for encoding in ("utf-8-sig", "utf-8", "gb18030", "utf-16"):
        try:
            return [(path.name, data.decode(encoding))]
        except UnicodeDecodeError:
            continue
    return [(path.name, data.decode("utf-8", errors="replace"))]


def xml_to_text(raw: bytes) -> str:
    try:
        root = ElementTree.fromstring(raw)
        return " ".join(piece.strip() for piece in root.itertext() if piece and piece.strip())
    except Exception:
        text = raw.decode("utf-8", errors="ignore")
        text = re.sub(r"<[^>]+>", " ", text)
        return html.unescape(re.sub(r"\s+", " ", text))


def office_members(ext: str) -> list[str]:
    if ext == ".docx":
        return ["word/", "docProps/"]
    if ext == ".xlsx":
        return ["xl/", "docProps/"]
    if ext == ".pptx":
        return ["ppt/", "docProps/"]
    return []


def read_office_file(path: Path) -> list[tuple[str, str]]:
    chunks: list[tuple[str, str]] = []
    prefixes = office_members(path.suffix.lower())
    with zipfile.ZipFile(path) as zf:
        for name in sorted(zf.namelist()):
            if not name.endswith(".xml"):
                continue
            if not any(name.startswith(prefix) for prefix in prefixes):
                continue
            text = xml_to_text(zf.read(name))
            if text.strip():
                chunks.append((name, text))
    return chunks


def read_pdf_file(path: Path) -> list[tuple[str, str]]:
    errors: list[str] = []
    try:
        from pypdf import PdfReader  # type: ignore

        reader = PdfReader(str(path))
        chunks = []
        for idx, page in enumerate(reader.pages, start=1):
            chunks.append((f"page {idx}", page.extract_text() or ""))
        if any(text.strip() for _, text in chunks):
            return chunks
        errors.append("pypdf extracted no text")
    except Exception as exc:
        errors.append(f"pypdf unavailable/failed: {exc}")

    try:
        from PyPDF2 import PdfReader  # type: ignore

        reader = PdfReader(str(path))
        chunks = []
        for idx, page in enumerate(reader.pages, start=1):
            chunks.append((f"page {idx}", page.extract_text() or ""))
        if any(text.strip() for _, text in chunks):
            return chunks
        errors.append("PyPDF2 extracted no text")
    except Exception as exc:
        errors.append(f"PyPDF2 unavailable/failed: {exc}")

    try:
        import pdfplumber  # type: ignore

        chunks = []
        with pdfplumber.open(str(path)) as pdf:
            for idx, page in enumerate(pdf.pages, start=1):
                chunks.append((f"page {idx}", page.extract_text() or ""))
        if any(text.strip() for _, text in chunks):
            return chunks
        errors.append("pdfplumber extracted no text")
    except Exception as exc:
        errors.append(f"pdfplumber unavailable/failed: {exc}")

    raise RuntimeError("UNSCANNABLE_PDF: " + " | ".join(errors))


def extract_text(path: Path) -> list[tuple[str, str]]:
    ext = path.suffix.lower()
    if is_text_path(path):
        return read_text_file(path)
    if ext in OFFICE_EXTENSIONS:
        return read_office_file(path)
    if ext in PDF_EXTENSIONS:
        return read_pdf_file(path)
    raise RuntimeError(f"Unsupported file extension: {ext}")


def load_allowlist(path: str | None) -> list[dict[str, str]]:
    if not path:
        return []
    data = json.loads(Path(path).read_text(encoding="utf-8-sig"))
    if not isinstance(data, list):
        raise ValueError("Allowlist must be a JSON list.")
    required = {"rule", "file", "text", "reason"}
    cleaned = []
    for idx, item in enumerate(data, start=1):
        if not isinstance(item, dict) or not required.issubset(item):
            raise ValueError(f"Allowlist entry {idx} must include rule, file, text, and reason.")
        if not str(item["reason"]).strip():
            raise ValueError(f"Allowlist entry {idx} has an empty reason.")
        cleaned.append({key: str(item[key]) for key in required})
    return cleaned


def load_custom_rules(paths: list[str] | None) -> list[Rule]:
    if not paths:
        return []
    flags = re.IGNORECASE | re.MULTILINE
    required = {"rule", "severity", "pattern", "suggestion"}
    custom_rules: list[Rule] = []
    for rule_path in paths:
        data = json.loads(Path(rule_path).read_text(encoding="utf-8-sig"))
        if not isinstance(data, list):
            raise ValueError(f"Rules file must be a JSON list: {rule_path}")
        for idx, item in enumerate(data, start=1):
            if not isinstance(item, dict) or not required.issubset(item):
                raise ValueError(
                    f"Rules entry {idx} in {rule_path} must include rule, severity, pattern, and suggestion."
                )
            name = str(item["rule"]).strip()
            severity = str(item["severity"]).strip().lower()
            pattern = str(item["pattern"])
            suggestion = str(item["suggestion"]).strip()
            if not name:
                raise ValueError(f"Rules entry {idx} in {rule_path} has an empty rule name.")
            if severity not in VALID_SEVERITIES:
                raise ValueError(
                    f"Rules entry {idx} in {rule_path} has invalid severity {severity!r}; use high, medium, or low."
                )
            if not suggestion:
                raise ValueError(f"Rules entry {idx} in {rule_path} has an empty suggestion.")
            try:
                compiled = re.compile(pattern, flags)
            except re.error as exc:
                raise ValueError(
                    f"Rules entry {idx} in {rule_path} has an invalid regex pattern: {exc}"
                ) from exc
            custom_rules.append(Rule(name, severity, compiled, suggestion))
    return custom_rules


def allow_reason(finding: Finding, allowlist: list[dict[str, str]]) -> str:
    normalized = finding.file.replace("\\", "/").lower()
    for entry in allowlist:
        entry_file = entry["file"].replace("\\", "/").lower()
        if entry["rule"] != finding.rule:
            continue
        if entry_file not in normalized:
            continue
        if entry["text"] not in finding.snippet:
            continue
        return entry["reason"]
    return ""


def line_col(text: str, index: int) -> tuple[int, int]:
    line = text.count("\n", 0, index) + 1
    last_newline = text.rfind("\n", 0, index)
    col = index + 1 if last_newline == -1 else index - last_newline
    return line, col


def make_snippet(text: str, start: int, end: int, width: int = 80) -> str:
    lo = max(0, start - width // 2)
    hi = min(len(text), end + width // 2)
    snippet = text[lo:hi].replace("\r", " ").replace("\n", " ")
    snippet = re.sub(r"\s+", " ", snippet).strip()
    return snippet


def scan_text(
    path: Path,
    source: str,
    text: str,
    allowlist: list[dict[str, str]],
    rules: list[Rule],
) -> list[Finding]:
    findings: list[Finding] = []
    for rule in rules:
        for match in rule.pattern.finditer(text):
            line, col = line_col(text, match.start())
            finding = Finding(
                file=normalize_path(path),
                location=f"{source}:line {line}, col {col}",
                severity=rule.severity,
                rule=rule.rule,
                snippet=make_snippet(text, match.start(), match.end()),
                suggestion=rule.suggestion,
            )
            reason = allow_reason(finding, allowlist)
            if reason:
                finding.allowed = True
                finding.reason = reason
            findings.append(finding)
    return findings


def scan_files(
    files: list[Path],
    allowlist: list[dict[str, str]],
    rules: list[Rule],
) -> tuple[list[Finding], list[ExtractionIssue]]:
    findings: list[Finding] = []
    issues: list[ExtractionIssue] = []
    for path in files:
        findings.extend(scan_text(path, "filename", path.name, allowlist, rules))
        try:
            chunks = extract_text(path)
            if not chunks:
                issues.append(ExtractionIssue(normalize_path(path), "NO_EXTRACTED_TEXT", "No text chunks were extracted."))
                continue
            for source, text in chunks:
                findings.extend(scan_text(path, source, text, allowlist, rules))
        except Exception as exc:
            detail = str(exc)
            if path.suffix.lower() == ".pdf" and detail.startswith("UNSCANNABLE_PDF"):
                findings.append(
                    Finding(
                        file=normalize_path(path),
                        location="file",
                        severity="high",
                        rule="UNSCANNABLE_PDF",
                        snippet=detail[:300],
                        suggestion="Export text or install a PDF extraction dependency, then rescan before release.",
                    )
                )
            else:
                issues.append(ExtractionIssue(normalize_path(path), "EXTRACTION_FAILED", detail[:500]))
    return findings, issues


def count_blocking(findings: list[Finding]) -> int:
    return sum(1 for f in findings if not f.allowed and f.severity in BLOCKING_SEVERITIES)


def write_json(path: str | None, payload: dict) -> None:
    if not path:
        return
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    Path(path).write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def write_markdown_report(
    path: str | None,
    *,
    title: str,
    payload: dict,
    findings: list[Finding],
    issues: list[ExtractionIssue],
    extra_sections: list[str] | None = None,
) -> None:
    if not path:
        return
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    lines = [
        f"# {title}",
        "",
        f"Generated: {payload['generated_at']}",
        f"Scanned files: {payload.get('scanned_file_count', 0)}",
        f"Blocking findings: {payload.get('blocking_findings', 0)}",
        f"Extraction issues: {len(issues)}",
        "",
        "## Findings",
        "",
    ]
    if findings:
        for idx, finding in enumerate(findings, start=1):
            status = "ALLOWLISTED" if finding.allowed else "ACTIVE"
            lines.extend(
                [
                    f"### {idx}. {finding.rule} ({finding.severity}, {status})",
                    "",
                    f"- File: `{finding.file}`",
                    f"- Location: {finding.location}",
                    f"- Snippet: `{finding.snippet}`",
                    f"- Suggestion: {finding.suggestion}",
                ]
            )
            if finding.allowed:
                lines.append(f"- Allowlist reason: {finding.reason}")
            lines.append("")
    else:
        lines.extend(["No rule findings.", ""])

    lines.extend(["## Extraction Issues", ""])
    if issues:
        for issue in issues:
            lines.extend([f"- `{issue.file}`: **{issue.issue}** - {issue.detail}"])
    else:
        lines.append("No extraction issues.")

    lines.extend(
        [
            "",
            "## Manual Public-Release Checklist",
            "",
            "- Confirm this is the intended public artifact, not a renamed internal draft.",
            "- Confirm visible text, filenames, metadata, comments, and hidden notes are public-clean.",
            "- Confirm no secrets, private paths, personal data, placeholders, review notes, or private IDs remain.",
            "- Confirm any AI-use or collaborator disclosure is deliberate and venue-appropriate.",
        ]
    )
    if extra_sections:
        lines.extend([""])
        for section in extra_sections:
            lines.extend([section, ""])
    Path(path).write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")


def command_scan(args: argparse.Namespace) -> int:
    allowlist = load_allowlist(args.allowlist)
    rules = RULES + load_custom_rules(args.rules)
    files, issues = collect_files(args.inputs)
    findings, extraction_issues = scan_files(files, allowlist, rules)
    issues.extend(extraction_issues)
    blocking = count_blocking(findings)
    payload = {
        "mode": "scan",
        "generated_at": dt.datetime.now(dt.timezone.utc).isoformat(),
        "inputs": args.inputs,
        "custom_rules": args.rules or [],
        "scanned_files": [normalize_path(path) for path in files],
        "scanned_file_count": len(files),
        "blocking_findings": blocking,
        "findings": [asdict(finding) for finding in findings],
        "extraction_issues": [asdict(issue) for issue in issues],
    }
    write_json(args.json, payload)
    write_markdown_report(args.report, title="Public Release Scan Report", payload=payload, findings=findings, issues=issues)
    if blocking:
        return 1
    if issues:
        return 2
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Scan files before public release.")
    subparsers = parser.add_subparsers(dest="command", required=True)

    scan = subparsers.add_parser("scan", help="Scan files or directories before public release.")
    scan.add_argument("inputs", nargs="+", help="Files or directories to scan.")
    scan.add_argument("--report", help="Markdown report path.")
    scan.add_argument("--json", help="JSON report path.")
    scan.add_argument("--rules", action="append", help="Optional JSON file with additional rule entries. Repeatable.")
    scan.add_argument("--allowlist", help="Optional JSON allowlist with rule, file, text, reason entries.")
    scan.set_defaults(func=command_scan)
    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    try:
        return int(args.func(args))
    except Exception as exc:
        sys.stderr.write(f"ERROR: {exc}\n")
        if os.environ.get("PUBLIC_RELEASE_SANITIZER_DEBUG"):
            traceback.print_exc()
        return 3


if __name__ == "__main__":
    raise SystemExit(main())

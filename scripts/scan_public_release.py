@dataclass
class Finding:
    file: str
    location: str
    severity: str
    rule: str
    snippet: str
    suggestion: str
    allowed: bool = False
    reason: str = ""


@dataclass
class ExtractionIssue:
    file: str
    issue: str
    detail: str


@dataclass(frozen=True)
class Rule:
    rule: str
    severity: str
    pattern: re.Pattern[str]
    suggestion: str


